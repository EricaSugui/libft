#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*cur;

	cur = (char *)s;
	while (n-- > 0)
	{
		if (*cur == c)
		{
			return (cur);
		}
		cur++;
	}
	return (NULL);
}
