#include <stdlib.h>
#include "libft.h"

void	ft_buffdel(t_buff **b)
{
	if (*b != NULL)
	{
		if ((*b)->c != NULL)
		{
			free((*b)->c);
		}
		free(*b);
		*b = NULL;
	}
}
